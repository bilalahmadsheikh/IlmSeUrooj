/**
 * Get scholarships relevant to a set of university IDs.
 * Returns scholarships that apply to any of the given universities,
 * plus government/general scholarships (empty universityIds).
 */
export function getScholarshipsForUniversities(universityIds = []) {
  if (!universityIds.length) return scholarships;
  return scholarships.filter(
    (s) =>
      s.universityIds.length === 0 ||
      s.universityIds.some((uid) => universityIds.includes(uid))
  );
}

export function getScholarshipsFiltered(categoryId) {
  let list = [...scholarships];
  if (categoryId && categoryId !== 'all') {
    list = list.filter((s) => s.type === categoryId);
  }
  return list;
}
